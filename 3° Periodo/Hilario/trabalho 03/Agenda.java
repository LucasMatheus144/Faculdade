package trabalho;

class Agenda {
	private int idade;
	private float altura;
	private String nome;
	private int posicao;
	
	public Agenda() {
	}
	
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public float getAlura() {
		return altura;
	}
	public void setAltura(float altura) {
		this.altura = altura;
	}
	
	public void setPosicao(int posicao) {
		this.posicao = posicao;
	}
	public int getPosicao() {
		return posicao;
	}
	
	
	
	
	
	
	
}